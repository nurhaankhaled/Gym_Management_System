    private void ViewClass()
    {
       for (Class c1 : GymMangement.classes)
       {
            if(c1.Classname==Classselectedvalue)
            {
                   
                classInfo=classInfo.concat("Name: "+c1.Classname + "\n" 
                +"Number of members in the class: "
                + String.valueOf(c1.Classmembers.size())
                + "\n" +"Assigned Trainer Name: "+c1.TrainerName
                + "\n" +"Assigned Trainer ID: "+String.valueOf(c1.trainerID));
            }           
       }      
       TrainersList.setText(classInfo);
       classInfo="";
    }

    private boolean Availability(Class cl,Trainer t1)
    {
        if(cl.Daytime.equals(t1.DayTime))
        {
            JOptionPane.showMessageDialog(null,"This Trainer is Already Assigned to a Class at the Same Time");
            return false;
        }
        else
        {
            return true;
        }
    }
