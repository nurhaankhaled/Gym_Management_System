    private void AssignButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AssignButtonActionPerformed
        IDOfTrainer=AssignTrainers.getText();
        boolean Exists=false;
        try
        {
            if(Integer.parseInt(IDOfTrainer) > Trainer.NumberOfTrainers)
            {
                JOptionPane.showMessageDialog(null,"There is no such Trainer.");
            }
            else 
            {

                for (Trainer t1 : GymMangement.trainers)
                {
                    String codestring = String.valueOf(t1.tranierID);

                    if(codestring.equals (IDOfTrainer))
                    {
                        Exists=true;
                        for (Class cl : GymMangement.classes)
                        {
                            if(cl.Classname.equals(Classselectedvalue))
                            {
                                if(Availability(cl, t1)==true)
                                {
                                    t1.DayTime=cl.Daytime;
                                    cl.TrainerName=t1.Name;
                                    cl.trainerID=t1.tranierID;
                                }

                            }

                        }
                        AssignTrainers.setText("");
                    }
                    if(Exists==false)
                    {
                        JOptionPane.showMessageDialog(null,"There is no such Trainer.");
                    }
                }
                ViewClass();            
            }
            
           
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Please enter a Trainer ID to Assign.");
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_AssignButtonActionPerformed

