    private void SearchIDButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchIDButtonActionPerformed
        boolean exists=false;
        try{IDSearch=Integer.parseInt(IDText.getText());}
        catch(Exception e){JOptionPane.showMessageDialog(null,"Please enter ID you want to search"); exists=true;}
        for (Member mem1 : GymMangement.members)
        {
            if(mem1.code==IDSearch)
            {
                exists=true;
                mem1.Dislayinfo(Entername, phonenumbertext, Agetext, Membershipbox, mem1.Name, mem1.phonenumber,mem1.Age,mem1.membership.Membershipname);
                price = String.valueOf(mem1.membership.price);
                Paymentlabel.setText(price);
            }
        }
        if(exists==false)
        {
            JOptionPane.showMessageDialog(null,"There is no such Member");
            ClearFields();
        }
    }//GEN-LAST:event_SearchIDButtonActionPerformed

