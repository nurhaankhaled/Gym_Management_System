    private void RemoveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveButtonActionPerformed
       try
       {
        int a=JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this member?" , "Select", JOptionPane.YES_NO_OPTION);
        if (a==0)
        { 
           
             IDSearch=Integer.parseInt(IDText.getText());
                try
                {   for (Member mem1 : GymMangement.members)
                    {
                        if(IDSearch==mem1.code)
                        {
                            removedmember=mem1;
                        }
                        ClearFields();
                    }
                    GymMangement.members.remove(removedmember);
                       
                }
                catch(Exception e)
                {
                    System.out.println(e.getMessage());
                }
        }
     
                   
       }
        catch(Exception e)
        {
           System.out.println(e.getMessage());
        }
        
    }//GEN-LAST:event_RemoveButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
    try{ IDSearch= Integer.parseInt(IDText.getText());}
    catch(Exception e){JOptionPane.showMessageDialog(null,"Please enter ID you want to update");}
            for (Member mem1 : GymMangement.members)
            {
                if(IDSearch==mem1.code)
                {
                    if(UpdateValidation()==true)
                    {
                        mem1.Name = Entername.getText();
                        try{ mem1.Age = Integer.parseInt(Agetext.getText().trim());}
                        catch(Exception e)
                        {
                            JOptionPane.showMessageDialog(null,"There was an error try again");
                        }
                        mem1.phonenumber=phonenumbertext.getText().trim();
                        
                        for (Membership memship1  : GymMangement.memberships)
                        {
                            if(memship1.Membershipname.equals(Packageselectedvalue))
                            {
                                mem1.membership = memship1;
                            }
                        }
                    ClearFields();
                    }
                }
            }
      
    }//GEN-LAST:event_updateButtonActionPerformed

